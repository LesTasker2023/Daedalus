'use client';

import { useState } from 'react';
import { Textarea } from '.';

export default function TextareaDemo() {
  const [state, setState] = useState(false);

  return (
    <div className="space-y-6 p-6 max-w-2xl mx-auto">
      <div className="space-y-2">
        <h1 className="text-2xl font-bold">Textarea Component</h1>
        <p className="text-muted-foreground">Multi-line text inputs</p>
      </div>

      <div className="space-y-4">
        <div className="space-y-2">
          <h3 className="text-lg font-semibold">Basic Usage</h3>
          <div className="border rounded-lg p-4 bg-background">
            <Textarea
              placeholder="Enter your text here..."
              defaultValue="This is a sample textarea"
            />
          </div>
        </div>

        <div className="space-y-2">
          <h3 className="text-lg font-semibold">Interactive</h3>
          <div className="border rounded-lg p-4 bg-background space-y-2">
            <Textarea
              value={state ? 'Active textarea content' : 'Inactive textarea'}
              onChange={() => setState(!state)}
              placeholder="Click to toggle state"
            />
            <p className="text-sm text-muted-foreground">State: {state ? 'Active' : 'Inactive'}</p>
          </div>
        </div>
      </div>
    </div>
  );
}
